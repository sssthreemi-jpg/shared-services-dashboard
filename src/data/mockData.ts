// ─── 회사 데이터 조립 ───
function buildCompany(
  id: string,
  name: string,
  areaScores: AreaScore[],
  tasks: Task[],
  previousScore: number | null,
  strengths: string[],
  weaknesses: string[],
  risks: string[],
  summary: string
): Company {
  const overallScore = calculateOverallScore(areaScores);
  return {
    id,
    name,
    areaScores,
    overallScore,
    grade: getGrade(overallScore),
    previousScore,
    tasks,
    strengths,
    weaknesses,
    risks,
    summary,
  };
}

